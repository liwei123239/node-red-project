const getDatabaseUrl = () => {
	const databaseUrl = process.env.DATABASE_URL;
	if (!databaseUrl) throw new Error("DATABASE_URL is required");
	return databaseUrl;
};
const handler = async () => {
	const db = drizzle(getDatabaseUrl());
	const statements = migrationSql.split("--> statement-breakpoint").map((statement) => statement.trim()).filter((statement) => statement.length > 0);
	for (const statement of statements) await db.execute(sql.raw(statement));
	return {
		statusCode: 200,
		body: JSON.stringify({ appliedStatements: statements.length })
	};
};
